sx = float(input("Введите стоимость 1кг конфет:  "));
sy= float(input("Введите стоимость 1кг печений: "));
sz = float(input("Введите стоимость 1кг яблок: "));
x = float(input("Введите кол-во конфет в кг: "));
y = float(input("Введите кол-во печений в кг:  "));
z = float(input("Введите кол-во яблок в кг: "));
sx=sx*x;
sy = sy*y;
sz=sz*z;
S = sx+sy+sz;
print("Стоимость всей покупки составляет ",S, "рублей");
